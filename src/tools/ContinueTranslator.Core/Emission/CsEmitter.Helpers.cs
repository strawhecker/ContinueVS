    /// <summary>
    /// Wraps <paramref name="members"/> in a file-scoped namespace declaration with using directives
    /// and returns the normalised source text with post-processing to improve formatting of anonymous object literals.
    /// </summary>
    /// <param name="ns">The namespace name (e.g., <c>"ContinueCore"</c>).</param>
    /// <param name="members">The member declarations (classes, enums, etc.) to include in the namespace.</param>
    /// <param name="imports">TypeScript imports that may contain .NET namespace references; using directives will be extracted from these.</param>
    /// <param name="standardUsings">Standard using namespaces (e.g., from System.Collections.Generic) to include unconditionally.</param>
    internal static string BuildCompilationUnit(string ns, IEnumerable<MemberDeclarationSyntax> members, TsImport[] imports, IEnumerable<string> standardUsings)
    {
        // Extract unique using directives from imports.
        // Note: Files with unmapped npm imports are filtered out by CsEmitter before emission,
        // so all imports here should be either relative paths or mapped to .NET namespaces.
        var usingNamespaces = new SortedSet<string>(StringComparer.Ordinal);

        // Add standard using statements (from collections, tasks, etc.)
        foreach (var standardUsing in standardUsings)
        {
            if (!string.IsNullOrWhiteSpace(standardUsing))
                usingNamespaces.Add(standardUsing);
        }

        foreach (var import in imports)
        {
            // Skip empty or whitespace-only module specifiers
            if (string.IsNullOrWhiteSpace(import.ModuleSpecifier))
                continue;

            // Skip relative imports (start with . or / after trimming)
            string moduleSpec = import.ModuleSpecifier.Trim();
            if (moduleSpec.StartsWith('.') || moduleSpec.StartsWith('/'))
                continue;

            // At this point, moduleSpec should be a mapped .NET namespace or type.
            // Unmapped npm packages should never reach here because files with unmapped imports 
            // are rejected during mapping phase. As a safety measure, we skip them:
            // - Plain lowercase packages without dots (e.g., "fs", "path")
            // - Scoped packages starting with @ (e.g., "@anthropic-ai/sdk", "@continuedev/config-yaml")
            // - Any package containing slashes (which are invalid in C# namespaces)
            if ((char.IsLower(moduleSpec[0]) && !moduleSpec.Contains('.')) ||
                moduleSpec.StartsWith('@') ||
                moduleSpec.Contains('/'))
                continue;

            // Add the module specifier as a using namespace (de-duplicated by SortedSet)
            usingNamespaces.Add(moduleSpec);
        }

        // Build using directives
        var usingDirectives = usingNamespaces
            .Select(ns => UsingDirective(ParseName(ns)))
            .Cast<UsingDirectiveSyntax>()
            .ToList();

        FileScopedNamespaceDeclarationSyntax nsDecl =
            FileScopedNamespaceDeclaration(ParseName(ns))
                .WithMembers(List(members))
                .NormalizeWhitespace();

        CompilationUnitSyntax cu = CompilationUnit()
            .WithUsings(List(usingDirectives))
            .WithMembers(SingletonList<MemberDeclarationSyntax>(nsDecl))
            .NormalizeWhitespace();

        string result = cu.ToFullString();

        // Post-process to fix formatting issues with anonymous object literals:
        // - Remove spaces before commas in object initializers ( , → ,)
        // - Collapse excessive newlines in nested object initializers into single lines
        result = FixAnonymousObjectFormatting(result);

        // Format closing braces on their own lines
        result = FormatClosingBraces(result);

        return result;
    }

    /// <summary>
    /// Post-processes generated C# code to improve formatting of anonymous object literals.
    /// Fixes issues like:
    /// - Spaces before commas: "value , " → "value,"
    /// - Excessive line breaks in nested objects
    /// - Missing 'new' keyword in nested anonymous object creations
    /// </summary>
     private static string FixAnonymousObjectFormatting(string code)
     {
         // Fix spaces before commas and closing braces in object initializers
         // Pattern: any whitespace (including newlines) followed by a comma
         code = System.Text.RegularExpressions.Regex.Replace(code, @"\s+,", ",");

         // Fix spaces before closing braces in object initializers
         // Pattern: any whitespace (including newlines) followed by }
         code = System.Text.RegularExpressions.Regex.Replace(code, @"\s+}", "}");

         // Collapse multiple blank lines into single blank lines
         code = System.Text.RegularExpressions.Regex.Replace(code, @"\n\s*\n\s*\n", "\n\n");

         // CRITICAL FIX: Convert property colons to equals in object property context.
         // Apply global regex-based conversion, being VERY careful about ternary operators.
         // 
         // Strategy: For each line, check if it contains a ternary operator (?...:)
         // If it does, only convert colons that appear AFTER the ternary's colon
         // If it doesn't, convert all identifier: patterns to identifier =
         //
         var lines = code.Split('\n');
         var processedLines = new System.Collections.Generic.List<string>();

         foreach (string line in lines)
         {
             string processedLine = line;

             // Check if this line is a switch case label (default or case statement)
             // Switch labels must start at the beginning of the line (after whitespace)
             bool isSwitchLabel = System.Text.RegularExpressions.Regex.IsMatch(
                 line, @"^\s*(?:default|case\b.*?)\s*:\s*(?://.*)?$");

             if (isSwitchLabel)
             {
                 // Preserve switch case labels — do not convert the colon
                 processedLine = line;
             }
             else if (line.Contains("?"))
             {
                 // If the line contains a ternary operator, be VERY conservative
                 // Find the position of the ? and the matching :
                 int questionPos = line.IndexOf('?');
                 int colonPos = line.IndexOf(':', questionPos);

                 if (colonPos > questionPos)
                 {
                     // There's a ternary colon. Only process colons AFTER it.
                     string beforeTernary = line.Substring(0, colonPos + 1);
                     string afterTernary = line.Substring(colonPos + 1);

                     // Convert colons in the after part
                     afterTernary = System.Text.RegularExpressions.Regex.Replace(
                         afterTernary,
                         @"\b([a-zA-Z_$][a-zA-Z0-9_$]*)\s*:\s*",
                         "$1 = ");

                     processedLine = beforeTernary + afterTernary;
                 }
                 else
                 {
                     // Has ? but no : after it, safe to convert all
                     processedLine = System.Text.RegularExpressions.Regex.Replace(
                         line,
                         @"\b([a-zA-Z_$][a-zA-Z0-9_$]*)\s*:\s*",
                         "$1 = ");
                 }
             }
             else
             {
                 // No ternary operator and not a switch label — safe to convert all identifier: patterns
                 processedLine = System.Text.RegularExpressions.Regex.Replace(
                     line,
                     @"\b([a-zA-Z_$][a-zA-Z0-9_$]*)\s*:\s*",
                     "$1 = ");
             }

             processedLines.Add(processedLine);
         }

         code = string.Join("\n", processedLines);

         // Fix: Inject 'new' keyword before opening braces in property assignments that are missing it.
         // This handles nested anonymous objects: "propertyName = {" → "propertyName = new {"
         // We match "= " followed by optional whitespace and "{", but NOT if preceded by "new"
         // RegexOptions.Multiline allows \s* to match newlines between = and {
         code = System.Text.RegularExpressions.Regex.Replace(
             code,
             @"(?<!new\s)=\s*\{",
             "= new {",
             System.Text.RegularExpressions.RegexOptions.Multiline);

         // Collapse redundant line breaks around single-line properties
         // E.g., "line = \n node.startPosition.row," -> "line = node.startPosition.row,"
         code = System.Text.RegularExpressions.Regex.Replace(
             code,
             @"(\s*=\s*)\n\s+([^\n{}\[\]]+,)",
             "$1 $2");

         return code;
     }

    /// <summary>
    /// Formats closing braces to appear on their own line.
    /// Converts code like "{ statement }" to "{\n  statement\n}" and similar patterns.
    /// This ensures proper C# formatting with closing braces on separate lines.
    /// 
    /// Special handling:
    /// - Do NOT split closing braces that are part of inline lambdas or delegates
    /// - Closing braces followed by ; are still split (they typically mark the end of a block)
    /// - Only skip splitting for property getters/setters that are inline
    /// </summary>
    private static string FormatClosingBraces(string code)
    {
        var lines = code.Split('\n');
        var result = new System.Collections.Generic.List<string>();

        foreach (var line in lines)
        {
            string trimmed = line.TrimEnd();

            // Only process lines that end with } and are not already just whitespace and }
            if (!trimmed.EndsWith("}") || trimmed.TrimStart().StartsWith("}"))
            {
                result.Add(line);
                continue;
            }

            // Find the position of the last closing brace
            int lastBracePos = trimmed.LastIndexOf('}');
            if (lastBracePos <= 0)
            {
                result.Add(line);
                continue;
            }

            // Check what comes before the last closing brace
            string beforeBrace = trimmed.Substring(0, lastBracePos).TrimEnd();

            // Skip if nothing before the brace (already on its own line)
            if (string.IsNullOrWhiteSpace(beforeBrace))
            {
                result.Add(line);
                continue;
            }

            // Check for patterns we should NOT split:
            // 1. Inline property getter/setter: { get; } or { get; init; } or { set; }
            if (beforeBrace.EndsWith("get") || beforeBrace.EndsWith("set") || beforeBrace.EndsWith("init"))
            {
                // This is a property accessor, keep inline
                result.Add(line);
                continue;
            }

            // 2. Single-expression lambda or delegate that should stay inline
            // Look for => pattern indicating a lambda
            if (beforeBrace.Contains("=>") && !beforeBrace.Contains("\n"))
            {
                // For now, keep lambdas inline - they're usually short expressions
                // But only if they're truly one-liners
                var statementCount = beforeBrace.Split(';').Length - 1;
                if (statementCount == 0)
                {
                    result.Add(line);
                    continue;
                }
            }

            // We have a case where code precedes the closing brace and it's not a special case
            // Extract indentation of the original line
            int indentLength = line.Length - trimmed.Length;
            string indent = line.Substring(0, indentLength);

            // Split: put the code on this line and the closing brace(s) on the next
            string afterBrace = trimmed.Substring(lastBracePos + 1);
            string codePart = beforeBrace + afterBrace;
            string braces = trimmed.Substring(lastBracePos);

            result.Add(indent + codePart);
            result.Add(indent + braces);
        }

        return string.Join("\n", result);
    }


