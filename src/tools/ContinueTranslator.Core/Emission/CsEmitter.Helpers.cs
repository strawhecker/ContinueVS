    /// <summary>
    /// Converts a camelCase variable name to PascalCase method name.
    /// Example: "generatorWithCancellation" → "GeneratorWithCancellation"
    /// </summary>
    private static string VariableNameToMethodName(string? varName)
    {
        if (string.IsNullOrEmpty(varName))
            return "__GeneratorFunc";

        // Capitalize the first character
        return char.ToUpperInvariant(varName[0]) + varName[1..];
    }

    /// <summary>
    /// Normalizes TypeScript property names to C# conventions, capitalizing only the most essential
    /// built-in properties that are guaranteed to be PascalCase across all .NET types:
    /// - length (arrays, strings, lists) → Length
    /// 
    /// For most other properties, we preserve the original case because:
    /// 1. Anonymous types created with lowercase names keep those lowercase property names
    /// 2. User-defined classes may have lowercase properties
    /// 3. The reflection-based HasProperty helper already uses IgnoreCase, so at runtime
    ///    the case mismatch is handled gracefully for legitimate use cases.
    /// </summary>
    private static string NormalizePropertyName(string? propertyName)
    {
        if (string.IsNullOrEmpty(propertyName))
            return propertyName ?? "__property";

        // Only capitalize "length" - it's universally PascalCase across built-in types:
        // string.Length, T[].Length, List<T>.Count (this one maps to Count in callsites)
        // All other common properties may vary depending on type, so we preserve case
        if (propertyName.Equals("length", StringComparison.OrdinalIgnoreCase))
            return "Length";

        // Preserve original case for all other properties
        return propertyName;
    }


    /// <summary>
    /// Collects required using statements based on the types found in the generated syntax members.
    /// This method walks the syntax tree to find type references and resolves their required namespaces.
    /// </summary>
    /// <param name="members">The member declarations to scan for type references.</param>
    /// <param name="usingsMap">The mapping from C# types to their required using statements.</param>
    /// <returns>A collection of required using namespace names (deduplicated).</returns>
    internal static IEnumerable<string> CollectRequiredUsings(IEnumerable<MemberDeclarationSyntax> members, UsingsMap usingsMap)
    {
        var requiredUsings = new HashSet<string>(StringComparer.Ordinal);

        foreach (var member in members)
        {
            CollectUsingsFromNode(member, usingsMap, requiredUsings);
        }

        return requiredUsings;
    }

    /// <summary>
    /// Recursively walks a syntax node and collects required using statements for all types found.
    /// This function only examines TypeSyntax nodes in contexts where they represent actual type declarations
    /// (properties, parameters, return types) rather than identifier usage.
    /// </summary>
    private static void CollectUsingsFromNode(SyntaxNode node, UsingsMap usingsMap, HashSet<string> requiredUsings)
    {
        // Process TypeSyntax, but ONLY if it's not a simple IdentifierNameSyntax 
        // (which would be a variable/parameter name reference, not a type declaration)
        // We include more complex types like GenericNameSyntax, QualifiedNameSyntax, etc.
        if (node is TypeSyntax typeSyntax && !(node is IdentifierNameSyntax))
        {
            string typeText = typeSyntax.ToFullString().Trim();
            if (!string.IsNullOrWhiteSpace(typeText))
            {
                // CRITICAL FIX: Roslyn's ToFullString() may include formatting spaces around angle brackets.
                // For example, ParseTypeSyntax("List<string>") produces a TypeSyntax that when converted 
                // to string may become "List < string >" with spaces.
                // Removing spaces ensures our type names match the keys in usings.json (e.g., "List<T>").
                string normalizedType = typeText.Replace(" ", string.Empty);

                var usings = usingsMap.Resolve(normalizedType);
                foreach (var @using in usings)
                {
                    requiredUsings.Add(@using);
                }
            }
        }

        // Recursively process all child nodes
        foreach (var child in node.ChildNodes())
        {
            CollectUsingsFromNode(child, usingsMap, requiredUsings);
        }
    }
