/**
 * Validate SHA256 checksum of an npm package file.
 *
 * Reads expected checksum from {packagePath}.sha256 file and compares
 * it to the computed SHA256 hash of the package file.
 *
 * @param {string} packagePath - Absolute path to .tgz file (e.g., 'continue-v2.0.0.tgz')
 * @param {string} checksumPath - Absolute path to .sha256 file (e.g., 'continue-v2.0.0.tgz.sha256')
 * @returns {Promise<Object>} Validation result:
 *   {
 *     valid: boolean,
 *     packagePath: string,
 *     checksumPath: string,
 *     computedHash: string,
 *     expectedHash: string,
 *     error: string | null
 *   }
 *
 * @example
 * const result = await validatePackageChecksum(
 *   'E:\\cache\\continue-v2.0.0.tgz',
 *   'E:\\cache\\continue-v2.0.0.tgz.sha256'
 * );
 *
 * if (result.valid) {
 *   console.log('✅ Package integrity verified');
 * } else {
 *   console.error(`❌ ${result.error}`);
 * }
 */
async function validatePackageChecksum(packagePath, checksumPath) {
  const result = {
    valid: false,
    packagePath,
    checksumPath,
    computedHash: '',
    expectedHash: '',
    error: null
  };

  try {
    // Step 1: Parse checksum file
    const { hash: expectedHash } = await parseChecksumFile(checksumPath);
    result.expectedHash = expectedHash;

    // Step 2: Compute actual hash
    const computedHash = await computeSHA256(packagePath);
    result.computedHash = computedHash;

    // Step 3: Compare hashes
    if (computedHash !== expectedHash) {
      result.error = (
        `Checksum mismatch. ` +
        `Expected: ${expectedHash}, ` +
        `Computed: ${computedHash}`
      );
      return result;
    }

    result.valid = true;
    return result;
  } catch (error) {
    result.error = error instanceof IntegrityError
      ? error.message
      : `Unexpected error: ${error.message}`;
    return result;
  }
}

/**
 * Validate manifest.json file for a package version.
 *
 * Checks:
 * - File exists and is valid JSON
 * - Required fields present: version, continueVersion, releaseDate, status, checksums
 * - Version matches expected value
 * - Checksums object contains 'sha256'
 *
 * @param {string} manifestPath - Absolute path to manifest.json
 * @param {string} expectedVersion - Expected version (e.g., '2.0.0' or 'v2.0.0')
 * @returns {Promise<Object>} Validation result:
 *   {
 *     valid: boolean,
 *     manifestPath: string,
 *     version: string | null,
 *     continueVersion: string | null,
 *     metadata: Object | null,
 *     error: string | null
 *   }
 *
 * @example
 * const result = await validateManifest(
 *   'E:\\cache\\manifest.json',
 *   '2.0.0'
 * );
 *
 * if (result.valid) {
 *   console.log(`✅ Manifest valid: Continue ${result.continueVersion}`);
 * } else {
 *   console.error(`❌ ${result.error}`);
 * }
 */
export async function validateManifest(manifestPath, expectedVersion) {
  const result = {
    valid: false,
    manifestPath,
    version: null,
    continueVersion: null,
    metadata: null,
    error: null
  };

  try {
    // Step 1: Load manifest
    const manifest = await loadManifest(manifestPath);
    result.version = manifest.version;
    result.continueVersion = manifest.continueVersion;

    // Step 2: Validate structure
    validateManifestStructure(manifest, expectedVersion);

    // Step 3: Store metadata
    result.metadata = {
      version: manifest.version,
      continueVersion: manifest.continueVersion,
      releaseDate: manifest.releaseDate,
      status: manifest.status,
      checksums: manifest.checksums
    };

    result.valid = true;
    return result;
  } catch (error) {
    result.error = error instanceof ManifestError
      ? error.message
      : `Unexpected error: ${error.message}`;
    return result;
  }
}

/**
 * Validate complete package integrity (checksum + manifest).
 *
 * This is the orchestrator function that combines checksum and manifest validation.
 * Both validations must pass for the result to be valid.
 *
 * Directory structure expected:
 * ```
 * versionDir/
 * ├── continue-{version}.tgz
 * ├── continue-{version}.tgz.sha256
 * └── manifest-{version}.json (or manifest.json)
 * ```
 *
 * @param {string} versionDir - Absolute path to version directory (e.g., '.cache/npm-packages/v2.0.0')
 * @param {string} version - Version string (e.g., '2.0.0' or 'v2.0.0')
 * @returns {Promise<Object>} Comprehensive validation result:
 *   {
 *     valid: boolean,
 *     version: string,
 *     versionDir: string,
 *     packagePath: string,
 *     manifestPath: string,
 *     checksumValid: boolean,
 *     manifestValid: boolean,
 *     metadata: {
 *       continueVersion: string,
 *       releaseDate: string,
 *       status: string
 *     },
 *     errors: string[]
 *   }
 *
 * @example
 * const result = await validatePackageIntegrity(
 *   'E:\\GitRepos\\ContinueVS\\.cache\\npm-packages\\v2.0.0',
 *   '2.0.0'
 * );
 *
 * if (result.valid) {
 *   console.log('✅ Package ready for launch');
 *   console.log(`   Continue: ${result.metadata.continueVersion}`);
 * } else {
 *   console.error('❌ Validation failed:');
 *   result.errors.forEach(err => console.error(`   - ${err}`));
 * }
 */
async function validatePackageIntegrity(versionDir, version) {
  // Normalize version (remove leading 'v')
  const versionNum = version.toString().replace(/^v/, '');
  const versionTag = version.toString().startsWith('v') ? version : `v${version}`;

  const result = {
    valid: false,
    version: versionNum,
    versionDir,
    packagePath: '',
    manifestPath: '',
    checksumValid: false,
    manifestValid: false,
    metadata: null,
    errors: []
  };

  // Construct file paths
  const packageName = `continue-${versionTag}.tgz`;
  const checksumName = `${packageName}.sha256`;
  const manifestName = `manifest-${versionTag}.json`;

  const packagePath = path.join(versionDir, packageName);
  const checksumPath = path.join(versionDir, checksumName);
  const manifestPath = path.join(versionDir, manifestName);

  result.packagePath = packagePath;
  result.manifestPath = manifestPath;

  // Validate checksum
  const checksumResult = await validatePackageChecksum(packagePath, checksumPath);
  result.checksumValid = checksumResult.valid;
  if (!checksumResult.valid) {
    result.errors.push(checksumResult.error);
  }

  // Validate manifest
  const manifestResult = await validateManifest(manifestPath, versionNum);
  result.manifestValid = manifestResult.valid;
  if (!manifestResult.valid) {
    result.errors.push(manifestResult.error);
  } else {
    result.metadata = manifestResult.metadata;
  }

  // Overall validity: both checks must pass
  result.valid = result.checksumValid && result.manifestValid;

  return result;
}

